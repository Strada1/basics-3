let addAction = document.getElementById('addAction').value;
let subtractAction = document.getElementById('subtractAction').value;
let multiAction = document.getElementById('multiAction').value;
let delayAction = document.getElementById('delayAction').value;
let buttonEqual = document.getElementById('buttonEqual');

function showResult() { 

    let firstNumber = document.getElementById('firstNumber').value;
    let secondtNumber = document.getElementById('secondtNumber').value;
    let allOperations = document.getElementById('allOperations').value;

    if (allOperations === addAction) {
        document.getElementById('result').textContent = Number(firstNumber) + Number(secondtNumber);
    } else if (allOperations === subtractAction) {
        document.getElementById('result').textContent = Number(firstNumber) - Number(secondtNumber);
    } else if (allOperations === multiAction) {
        document.getElementById('result').textContent = Number(firstNumber) * Number(secondtNumber);
    } else if (allOperations === delayAction) {
        document.getElementById('result').textContent = Number(firstNumber) / Number(secondtNumber);
    } 
}

buttonEqual.addEventListener('click', showResult)  

















































































































































































































































































