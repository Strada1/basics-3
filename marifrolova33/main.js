//Вывести в консоль числа от 1 до 30 с помощью цикла
let k = 1;
while (k < 31) {
  console.log(k);
  k = k + 1;
}

//Вывести в консоль числа от 1 до 30 с помощью цикла
//но с проверкой условия после тела цикла (do ... while)
let l = 1;
do {
  console.log(l);
  l = l + 1;
} while (l < 31);

//Вывести в консоль числа от 1 до 30 с помощью  цикла  for //
for (let i = 1; i < 31; i++) {
  console.log(i);
}
