//Вывести в консоль числа от 1 до 19 с помощью цикла
let k = 1;
while (k < 20) {
  console.log(k);
  k = k + 1;
}

//Вывести в консоль числа от 1 до 19 с помощью цикла
//но с проверкой условия после тела цикла (do ... while)
let l = 1;
do {
  console.log(l);
  l = l + 1;
} while (l < 20);

//Вывести в консоль числа от 1 до 19 с помощью  цикла  for //
for (let i = 1; i < 20; i++) {
  console.log(i);
}
